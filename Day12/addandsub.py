#  Adding and Subtracting Matrices in Python
import numpy as np

# Define matrices
A = np.array([[1, 2], [3, 4]])
B = np.array([[5, 6], [7, 8]])

# Perform addition and subtraction
addition = A + B
subtraction = A - B

# Print results
print("Matrix Addition:")
print(addition)
print("\nMatrix Subtraction:")
print(subtraction)
